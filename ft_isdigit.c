#include "push_swap.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (!str[i])
		return (0);
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		i++;
	if (ft_atoi(str) == 2147483648 || ft_atoi(str) == -2147483649)
		return (0);
	return (str[i] == '\0');
}
